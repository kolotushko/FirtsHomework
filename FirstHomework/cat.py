class Pet:
  def __init__(self, name, hunger, joy, age):
      self.Name = name
      self.Hunger = hunger
      self.Joy = joy
      self.Age = age
  def ToString(self):
      print(f"Name: {self.Name}\n"
            f"Hunger: {self.Hunger}%\n"
            f"Joy{self.Joy}%\n"
            f"Age: {self.Age}\n")









